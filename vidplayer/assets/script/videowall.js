


// Display video suggestion wall. 
function toggleVideoWall() {

	// 
	vidplayer['box'].classList.toggle('wall');
}

// Display video suggestion wall. 
function displayVideoWall() {

	// 
	vidplayer['box'].classList.add('wall');
}

// Hide video suggestion wall. 
function hideVideoWall() {

	// 
	vidplayer['box'].classList.remove('wall');
}
