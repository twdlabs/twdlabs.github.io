function getProjectById(pid) {

	// Go thru all projects. 
	for(let project of projectData) {

		// Check for match. 
		let matchFound = (project.projectid == pid);

		// Return match when found. 
		if(matchFound) return project;
	}

	// Return nothing if not found. 
	return null;
}

